class DiceBagConfiguration {
  _blue;
  _green;
  _red;
  _purple;
  _yellow;
  _draftSize;

  constructor(blue, green, red, purple, yellow, draftSize) {
    this._blue = blue;
    this._green = green;
    this._red = red;
    this._purple = purple;
    this._yellow = yellow;
    this._draftSize = draftSize;
  }
}

export default DiceBagConfiguration;
